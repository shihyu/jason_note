import math  # 导入math模块

print("-82的绝对值：", abs(-82))
print("12.6的上入整数：", math.ceil(4.6))
print("12.6的下入整数：", math.floor(4.6))
print("12.6的四舍五入整数：", round(4.6))
print("e的3次幂：", math.exp(3))
print("以10为基数的1000的对数：", math.log10(1000))
print("6的4次方：", math.pow(6, 4))
print("49的平方根：", math.sqrt(49))
print("2、5、12、15数中的最大数", max(2, 5, 12, 15))
print("2、5、12、15数中的最小数", min(2, 5, 12, 15))
