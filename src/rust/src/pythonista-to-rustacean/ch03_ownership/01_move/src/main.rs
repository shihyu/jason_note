//! # Move Semantics：所有權的轉移
//!
//! 本範例展示 Rust 的**移動語意**——當值被賦予給另一個變數時，**所有權會轉移**，
//! 原本的變數隨即失效。
//!
//! ## 移動 vs 複製
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────────┐
//! │                    Move vs Copy 視覺化                             │
//! ├─────────────────────────────────────────────────────────────────┤
//! │                                                                 │
//! │  Copy 型別（如 i32, u8, bool）                                │
//! │  ─────────────────────────────────────                         │
//! │  let a: i32 = 42;                                           │
//! │  let b = a;          // 值被「複製」，a、b 都有效              │
//! │  println!("{}", a); // ✅ 正常                               │
//! │                                                                 │
//! │  Move 型別（如 String, Vec, 自訂 struct）                     │
//! │  ─────────────────────────────────────                         │
//! │  let s1 = String::from("hello");                              │
//! │  let s2 = s1;          // 所有權「移動」到 s2，s1 失效        │
//! │  println!("{}", s1); // ❌ 編譯錯誤！s1 已無效              │
//! └─────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## Python vs Rust：所有權對照
//!
//! | 情境 | Python | Rust |
//! |------|---------|------|
//! | 簡單型別賦值 | `b = a`（複製）| `let b = a`（複製）|
//! | 物件賦值 | `b = a`（參照相同）| `let b = a`（**移動**）|
//! | 函式參數 | 參照傳遞 | 預設**移動**語意 |
//! | 借用（不移動）| `b = a`（不移動）| `let b = &a`（借用）|
//!
//! ## 移動的生命週期
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────────┐
//! │                    所有權轉移的生命週期                           │
//! │                                                                 │
//! │   let my_drink = Sugar::TainanStyle;  // my_drink 擁有所有權   │
//! │         │                                                       │
//! │         ▼                                                       │
//! │   display_sugar(my_drink);              // 所有權「移動」到參數  │
//! │         │                                                       │
//! │         ▼                                                       │
//! │   // my_drink 已失效！                                         │
//! │   display_sugar(my_drink);              // ❌ 編譯錯誤！       │
//! │                                                                 │
//! │   // 移動後的生命週期：                                        │
//! │   fn display_sugar(sugar: Sugar) {    // sugar 取得所有權    │
//! │       ...                                                       │
//! │   } // sugar 離開作用域，值被 drop                            │
//! └─────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 執行方式
//!
//! ```bash
//! cd ch03_ownership/01_move
//! cargo run
//! ```

// 定義飲料甜度
enum Sugar {
    Regular,
    TainanStyle, // 台南甜度 ( 帶著在地人情味的甜)
}

// 此函式會取得傳入參數的所有權
fn display_sugar(sugar: Sugar) {
    match sugar {
        Sugar::Regular => println!("Regular sweet"),
        Sugar::TainanStyle => println!("Tainan sweet"),
    }
} // sugar 在此離開作用域，其擁有的值被 drop

fn main() {
    let my_drink = Sugar::TainanStyle; // my_drink 成為擁有者
    // 第一次呼叫：
    display_sugar(my_drink); // 所有權從 my_drink「移動」至 sugar，my_drink 失效
    // 第二次呼叫：
    // display_sugar(my_drink); // 錯誤：嘗試使用一個已失效的值
}