//! # Rust 程式設計的第一個步伐
//!
//! ## 📖 概念說明
//!
//! 這個範例展示 Rust 程式最基礎的結構：建立一個可執行的 Rust 專案。
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────┐
//! │  Rust 程式執行流程 (Python 對比)                       │
//! ├─────────────────────────────────────────────────────────┤
//! │                                                         │
//! │   Python              Rust                             │
//! │   ─────────           ─────────                        │
//! │   python file.py  →  cargo run  (或直接執行編譯後的 binary)  │
//! │       │                     │                          │
//! │       ▼                     ▼                          │
//! │   直譯器逐行執行      先編譯成機器碼                  │
//! │                       再執行編譯後的檔案                 │
//! │                                                         │
//! │   💡 Rust 是編譯語言，效能通常比直譯語言快！           │
//! └─────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 🏗️ Cargo 專案結構
//!
//! ```text
//! my_project/           ← 專案根目錄（Cargo.toml 所在位置）
//! ├── Cargo.toml       ← 專案設定檔（相依套件、元資料）
//! └── src/
//!     └── main.rs       ← 程式進入點（main 函式）
//! ```
//!
//! ## 🔑 關鍵語法
//!
//! | Rust 語法 | 說明 |
//! |-----------|------|
//! | `fn main()` | 程式的入口點，系統會自動呼叫這個函式 |
//! | `println!()` | 巨集（macro），用於輸出文字到終端機 |
//!
//! ## 📝 與 Python 的對比
//!
//! | Python | Rust |
//! |--------|------|
//! | `def main():` | `fn main()` |
//! | `print("Hello")` | `println!("Hello")` |
//! | 直接執行 `.py` | 需先 `cargo build` 再執行或 `cargo run` |
//!
//! ## ⚡ 執行方式
//!
//! ```bash
//! cd ch01_basics/01_rust_intro
//! cargo run
//! ```
//!
//! 預期輸出：
//! ```text
//! Hello, Performance!
//! ```

fn main() {
    println!("Hello, Performance!");
}
