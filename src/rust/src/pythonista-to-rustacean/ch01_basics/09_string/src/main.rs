//! # String（字串）
//!
//! ## 📖 概念說明
//!
//! Rust 的字串系統比 Python 複雜，主要有 `String`（可變動）和 `&str`（不可變動的借用）。
//!
//! ```text
//! ┌──────────────────────────────────────────────────────────────────────┐
//! │  String vs &str 記憶體模型                                          │
//! ├──────────────────────────────────────────────────────────────────────┤
//! │                                                                      │
//! │  String（擁有所有權）                                                 │
//! │  ┌──────────────────────────────────────┐                           │
//! │  │   heap: "hello"                    │ ← 資料存在 heap            │
//! │  │   capacity: 5                       │ ← 已分配記憶體容量         │
//! │  │   ptr: 0x7f...                     │ ← 指標位置                │
//! │  └──────────────────────────────────────┘                           │
//! │                                                                      │
//! │  &str（借用，視圖）                                                  │
//! │  ┌────────────┐                                                     │
//! │  │  ptr: ... │ ───────────────────────────┐                        │
//! │  │  len: 5   │                          │                         │
//! │  └────────────┘                          ▼                         │
//! │                                      "hello" (可能在 stack/heap)    │
//! │                                                                      │
//! └──────────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 🔑 型別對照
//!
//! | 型別 | 說明 | 可變性 |
//! |------|------|--------|
//! | `String` | 擁有資料所有權 | 可變 |
//! | `&str` | 字串切片引用 | 不可變 |
//! | `&mut str` | 可變借用 | 可變 |
//!
//! ## 📝 與 Python 的對比
//!
//! | Python | Rust |
//! |--------|------|
//! | `s = "hello"` | `let s: &str = "hello";` |
//! | `s = str()` | `let s = String::new();` |
//! | `s += "world"` | `s.push_str("world")` 或 `s = s + &s2` |
//! | `s1 + s2` | `format!("{}-{}", s1, s2)` |
//!
//! ## ⚡ 常見操作
//!
//! - `.push_str()`：附加字串
//! - `.push()`：附加單一字元
//! - `+` 運算子：串接（會移動所有權）
//! - `format!()`：格式化串接（不移動所有權）

fn main() {
    // 建立一個空字串
    let mut s = String::new();
    // 從字串字面值 (literal, 型別為 &str) 轉換
    let s1 = "hello".to_string();
    let s2 = String::from("world");

    let mut s = String::from("foo");
    s.push_str("bar"); // s 現在是 "foobar"
    s.push('!'); // s 現在是 "foobar!"

    let s1 = String::from("Hello, ");
    let s2 = String::from("world!");
    // s1 在這裡被「移動」了，s1 的所有權轉移給了 s3
    let s3 = s1 + &s2;
    // println!("{}", s1); // <-- 這行程式碼會編譯失敗！
    println!("{}", s2); // s2 只是被借用，依然有效
    println!("{}", s3); // s3 是新的擁有者

    let s1 = String::from("tic");
    let s2 = String::from("tac");
    let s3 = String::from("toe");
    // s1, s2, s3 在這裡都只是被借用，之後仍然可用
    let s = format!("{}-{}-{}", s1, s2, s3);
}
