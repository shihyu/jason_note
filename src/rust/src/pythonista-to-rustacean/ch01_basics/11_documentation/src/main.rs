//! # Documentation（文件註解）
//!
//! ## 📖 概念說明
//!
//! Rust 提供強大的文件系統，使用 `///` 或 `//!` 語法可以為程式碼添加說明文件。
//!
//! ```text
//! ┌──────────────────────────────────────────────────────────────────────┐
//! │  文件註解語法                                                      │
//! ├──────────────────────────────────────────────────────────────────────┤
//! │                                                                      │
//! │  /// 外部文件註解（置於函式/結構體之前）                              │
//! │  │  /// 這是針對外在使用者的說明                                      │
//! │  │  /// ```                                                         │
//! │  │  /// let x = add_one(5);                                        │
//! │  │  /// ```                                                         │
//! │  │  fn add_one(x: i32) -> i32 { x + 1 }                          │
//! │                                                                      │
//! │  //! 內部文件註解（置於模組/檔案開頭）                               │
//! │  │  //! # 模組說明                                                  │
//! │  │  //! 此模組提供基本功能                                           │
//! │                                                                      │
//! │  // 的一般註解（給程式設計師看的備忘）                               │
//! │     // TODO: 優化效能                                               │
//! │                                                                      │
//! └──────────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 🔑 文件註解類型
//!
//! | 語法 | 用途 | 產生位置 |
//! |------|------|----------|
//! | `/// ...` | 外部文件（對外 API） | RustDoc HTML |
//! | `//! ...` | 內部文件（模組說明） | 適用於 `lib.rs` |
//! | `// ...` | 一般備註 | 不進入文件 |
//!
//! ## 📝 文件生成
//!
//! ```bash
//! cargo doc --open  # 生成文件並在瀏覽器打開
//! ```
//!
//! ## ⚡ 文件測試
//!
//! 文件中的程式碼區塊可作為測試：
//!
//! ```bash
//! cargo test --doc  # 執行文件中的範例程式碼
//! ```

/// # Examples
///
/// ```
/// let arg = 5;
/// let answer = my_crate::add_one(arg);
///
/// assert_eq!(6, answer);
/// ```
fn add_one(x: i32) -> i32 {
    x + 1
}

