x = 523  # 整型变量x，并赋值为523
y = 36  # 整型变量y，并赋值为36
print("\n变量x的初始值为:", x, "   变量y的初始值为：", y)
y += x
print("\n\ny += x后，变量y的值为：", y)
y -= x
print("y -= x后，变量y的值为：", y)
y *= x
print("y *= x后，变量y的值为：", y)
y /= x
print("y /= x后，变量y的值为：", y)
y %= x
print("y %= x后，变量y的值为：", y)
y //= x
print("y //= x后，变量y的值为：", y)
y **= x
print("y **= x后，变量y的值为：", y)
