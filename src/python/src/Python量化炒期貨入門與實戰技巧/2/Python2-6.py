num1 = int(input("请输入第一个数："))  # 用于计算的第一个数
num2 = int(input("请输入第二个数："))  # 用于计算的第二个数
print("第一个数是：", num1, "第二个数是：", num2)
print()
print("两个数相加：", num1 + num2)
print("两个数相减：", num1 - num2)
print("两个数相乘：", num1 * num2)
print("两个数相除：", num1 / num2)
print("两个数相除，求余数，即取模：", num1 % num2)
print("两个数相除，但只取商的整数部分：", num1 // num2)
print("幂，即返回num1的num2次幂：", num1 ** num2)
