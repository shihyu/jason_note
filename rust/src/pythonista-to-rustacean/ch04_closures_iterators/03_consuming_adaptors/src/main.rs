//! # `03_consuming_adaptors` - 消費型 Adaptor
//!
//! ## 概念說明
//!
//! **消費型 adaptor（Consuming Adaptors）**會呼叫 `.next()`
//! 反覆取用迭代器中的元素，直到迭代器耗盡（回傳 `None`）。
//! 這些 adaptor **會取得迭代器的所有權**，因此呼叫後迭代器就無法再使用。
//!
//! ```text
//! ┌──────────────────────────────────────────────────────────┐
//! │  常見消費型 adaptor                                        │
//! ├──────────────────────────────────────────────────────────┤
//! │                                                          │
//! │  .sum()     ──►  將所有元素加總                           │
//! │  .product() ──►  將所有元素相乘                          │
//! │  .collect() ──►  收集到另一個集合                        │
//! │  .fold()    ──►  自訂初始值的歸納                        │
//! │  .count()   ──►  計算元素數量                           │
//! │  .last()    ──►  取最後一個元素                          │
//! │                                                          │
//! │  ┌─────────────────────────────────────────┐              │
//! │  │  numbers.iter().sum() 內部流程           │              │
//! │  │                                          │              │
//! │  │  iter: Iterator<&i32>                    │              │
//! │  │      │                                  │              │
//! │  │      ▼ .next()                          │              │
//! │  │  Some(&1) ──► acc = 0 + 1 = 1          │              │
//! │  │  Some(&2) ──► acc = 1 + 2 = 3          │              │
//! │  │  Some(&3) ──► acc = 3 + 3 = 6          │              │
//! │  │  Some(&4) ──► acc = 6 + 4 = 10         │              │
//! │  │  Some(&5) ──► acc = 10 + 5 = 15        │              │
//! │  │  None     ──► 回傳 15                   │              │
//! │  │              (迭代器已耗盡)              │              │
//! │  └─────────────────────────────────────────┘              │
//! └──────────────────────────────────────────────────────────┘
//! ```
//!
//! ## Python 對照表
//!
//! | 概念 | Python | Rust |
//! |---|---|---|
//! | 加總 | `sum(iterable)` | `.sum()` |
//! | 迭代器耗盡 | `sum()` 後原物件仍可用（Python 迭代器不同） | 消費後迭代器失效 |
//! | 取最大值 | `max(iterable)` | `.max()` |
//! | 收集到列表 | `list(map(...))` | `.collect::<Vec<_>>()` |
//! | 自訂歸納 | `functools.reduce(func, iterable, init)` | `.fold(init, \|acc, x\| ...)` |
//!
//! ## 重點解析
//!
//! - 消費型 adaptor **取得迭代器所有權**，呼叫後原迭代器不再可用
//! - `.sum()` 泛型回傳型別由**上下文推導**，需指定如 `let total: i32`
//! - Rust 迭代器是**消耗性**的；Python 的 `map`/`filter` 物件可重複建立
//!
//! ## 執行方式
//!
//! ```bash
//! cargo run
//! ```
//!
//! ## 預期輸出
//!
//! ```text
//! Total: 15
//! ```

fn main() {
    let numbers = vec![1, 2, 3, 4, 5];
    // numbers.iter() 建立一個迭代器
    // .sum() 會取得該迭代器的所有權，並消耗它
    let total: i32 = numbers.iter().sum(); // .sum() 內部會不斷呼叫 .next() 並加總
    println!("Total: {}", total); // Total: 15 ( 註：1+2+3+4+5 = 15)
    // 此時 .iter() 產生的那個迭代器已經被 .sum() 消耗掉了
}