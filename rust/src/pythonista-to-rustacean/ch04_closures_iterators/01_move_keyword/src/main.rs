//! # `01_move_keyword` - `move` 關鍵字與執行緒
//!
//! ## 概念說明
//!
//! 閉包（Closure）可以使用 `move` 關鍵字**強制取得**其捕獲的環境變數的所有權。
//! 這在需要將變數的所有權轉移給另一執行緒時特別重要。
//!
//! ```text
//! ┌──────────────────────────────────────────────────────┐
//! │  主執行緒                                               │
//! │                                                       │
//! │   let list = vec![1, 2, 3]  ──►  ┌───────────┐     │
//! │                                   │  Vec<i32> │     │
//! │                                   └───────────┘     │
//! │                                   所有權在主執行緒   │
//! │                                                       │
//! │   thread::spawn(move || {        ──►  所有權轉移     │
//! │       println!("{:?}", list)      到新執行緒        │
//! │   })                                                │
//! │                         ┌───────────┐                │
//! │                         │  Vec<i32> │                │
//! │                         │ (已移轉)  │                │
//! │                         └───────────┘                │
//! └──────────────────────────────────────────────────────┘
//! ```
//!
//! ## Python 對照表
//!
//! | 概念 | Python | Rust |
//! |---|---|---|
//! | 執行緒建立 | `threading.Thread(target=func, args=(data,))` | `thread::spawn(move \|\| { ... })` |
//! | 資料傳遞給執行緒 | 傳遞物件（共享參考） | `move` 閉包取得所有權 |
//! | 所有權問題 | 無（GC 自動管理） | `move` 是所有權轉移，不是複製 |
//! | 執行緒_join | `thread.join()` | `.join().unwrap()` |
//!
//! ## 重點解析
//!
//! - `move ||` 讓閉包取得 `list` 的**所有權**
//! - 若沒有 `move`，編譯會失敗，因為執行緒的生命週期可能短於主執行緒
//! - `list` 在 `move` 後在主執行緒中**已失效**，無法再使用
//!
//! ## 執行方式
//!
//! ```bash
//! cargo run
//! ```
//!
//! ## 預期輸出
//!
//! ```text
//! Before defining closure: [1, 2, 3]
//! From thread: [1, 2, 3]
//! ```

use std::thread;

fn main() {
    let list = vec![1, 2, 3];
    println!("Before defining closure: {:?}", list);
    // 建立一個新執行緒
    thread::spawn(move || println!("From thread: {:?}", list))
        .join()
        .unwrap();
}