//! # Flow Control（流程控制）
//!
//! ## 📖 概念說明
//!
//! Rust 的流程控制包含 `if/else` 條件判斷、`match` 模式配對、以及各種迴圈結構。
//!
//! ```text
//! ┌──────────────────────────────────────────────────────────────────┐
//! │  match 表達式流程圖                                              │
//! ├──────────────────────────────────────────────────────────────────┤
//! │                                                                  │
//! │    ┌─────────────┐                                             │
//! │    │  "left"    │                                             │
//! │    └──────┬──────┘                                             │
//! │           ▼                                                     │
//! │    ┌─────────────┐                                             │
//! │    │ match { }  │ ←─ 根據值尋找對應的分支                     │
//! │    └──────┬──────┘                                             │
//! │           │                                                     │
//! │     ┌─────┼─────┬─────────┐                                    │
//! │     ▼     ▼     ▼         ▼                                    │
//! │  "left" "right"  "_" (其他)                                   │
//! │     │     │     │         │                                    │
//! │     ▼     ▼     ▼         ▼                                    │
//! │    -1    +1     0         │  ← 回傳值                          │
//! │                                                                  │
//! └──────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 🔑 match 與 Python match-case 的對比
//!
//! | Python 3.10+ | Rust |
//! |---------------|------|
//! | `match command:` | `match command {` |
//! | `case "left":` | `"left" =>` |
//! | `case _:` | `_ =>` (萬用符) |
//!
//! ## 📝 match 的優勢
//!
//! - **必須涵蓋所有可能**：編譯器會檢查是否處理了所有情況
//! - **支援模式配對**：可配對範圍、Tuple、Enum 等複雜結構
//! - **回傳值**：每個分支可以回傳不同的型別或值

fn main() {
    let command = "left";
    let direction = match command {
        "left" => -1,
        "right" => 1,
        _ => 0, // _ 是一個特殊的萬用 (catch-all) 模式
    };

    println!("Direction offset: {direction}"); // 印出 -1
}
