//! # Array（陣列）與 Slice（切片）
//!
//! ## 📖 概念說明
//!
//! Array 是 Rust 中**固定長度**、**同質型別**（所有元素型別相同）的資料結構。Slice 則是對 Array 的**引用視圖**。
//!
//! ```text
//! ┌──────────────────────────────────────────────────────────────────────┐
//! │  Array 與 Slice 的關係                                              │
//! ├──────────────────────────────────────────────────────────────────────┤
//! │                                                                      │
//! │  Array（完整所有權）                                                  │
//! │  ┌─────┬─────┬─────┬─────┬─────┬─────┐                           │
//! │  │ 'a' │ 'b' │ 'c' │ 'd' │ 'e' │ 'f' │  ← 陣列本體，擁有所有元素   │
//! │  └─────┴─────┴─────┴─────┴─────┴─────┘                           │
//! │    0     1     2     3     4     5    ← 索引                   │
//! │                                                                      │
//! │  Slice（借用視圖）                                                   │
//! │  ┌───────────────────────────┐                                      │
//! │  │      &chars[1..5]       │  ← 借用 indices 1~4 的元素           │
//! │  └───────────────────────────┘                                      │
//! │         │                  │                                        │
//! │         ▼                  ▼                                        │
//! │      ['b', 'c', 'd', 'e']  ← Slice 指向原 Array 的這段資料         │
//! │                                                                      │
//! └──────────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 🔑 語法對照
//!
//! | 語法 | 說明 | 範例 |
//! |------|------|------|
//! | `[T; N]` | Array 型別（T 為元素型別，N 為長度） | `[i32; 6]` |
//! | `&[T]` | Slice 型別（對 Array 的引用） | `&[char]` |
//! | `[start..end]` | Range 切片語法 | `&arr[1..5]` |
//!
//! ## 📝 與 Python 的對比
//!
//! | Python | Rust |
//! |--------|------|
//! | `arr = ['a', 'b', 'c', 'd', 'e', 'f']` | `let arr: [char; 6] = ['a', 'b', 'c', 'd', 'e', 'f'];` |
//! | `arr[1:5]` | `&arr[1..5]` |
//! | `len(arr)` | `arr.len()` |
//!
//! ## ⚡ 重要特性
//!
//! - Array 長度在編譯時就確定，無法動態調整
//! - Slice 是借用，不擁有資料所有權
//! - Slice 可從 Array、Vec、String 建立

fn main() {
    let chars: [char; 6] = ['a', 'b', 'c', 'd', 'e', 'f'];

    // 切出索引 1 到 5 ( 不含 5)，得到 ['b', 'c', 'd', 'e']
    let slice: &[char] = &chars[1..5];

    // 從切片中再切出子切片，索引 1 到 3 ( 不含 3)，得到 ['c', 'd']
    let sub_slice: &[char] = &slice[1..3];
    println!("{:?}", slice);
    println!("{:?}", sub_slice);
}
