//! # Tuple（元組）
//!
//! ## 📖 概念說明
//!
//! Tuple 是 Rust 中一種**固定長度**、**異質型別**（不同型別可以放在一起）的資料結構。
//!
//! ```text
//! ┌──────────────────────────────────────────────────────────────────┐
//! │  Tuple 記憶體佈局                                               │
//! ├──────────────────────────────────────────────────────────────────┤
//! │                                                                  │
//! │  let data = ("Rust", 2025, true);                              │
//! │                                                                  │
//! │  ┌────────────┬────────────┬────────────┐                        │
//! │  │  index: 0  │  index: 1  │  index: 2  │                        │
//! │  ├────────────┼────────────┼────────────┤                        │
//! │  │  "Rust"    │    2025    │   true     │                        │
//! │  │  &str      │    i32     │   bool     │                        │
//! │  └────────────┴────────────┴────────────┘                        │
//! │                                                                  │
//! │  💡 Tuple 長度固定，不能新增/刪除元素                             │
//! │                                                                  │
//! └──────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 🔑 存取方式
//!
//! | 方式 | 語法 | 範例 |
//! |------|------|------|
//! | 索引存取 | `.0`, `.1`, `.2` | `data.0` 取得 "Rust" |
//! | 解構 | `let (a, b, c) = data` | 同時取出所有元素 |
//!
//! ## 📝 與 Python 的對比
//!
//! | Python | Rust |
//! |--------|------|
//! | `data = ("Rust", 2025, True)` | `let data = ("Rust", 2025, true);` |
//! | `data[0]` | `data.0` |
//! | `a, b, c = data` | `let (a, b, c) = data;` |
//!
//! ## ⚡ 何時使用 Tuple？
//!
//! - 函式回傳多個不同型別的值
//! - 臨時組合相關資料
//! - 模式配對（pattern matching）

fn main() {
    let data = ("Rust", 2025, true);

    // 方法一：透過句號 . 加索引值取得
    println!("Language: {}", data.0); // Rust
    println!("Year: {}", data.1); // 2025
    println!("Is awesome: {}", data.2); // true
    // 方法二：使用模式配對 (pattern matching) 進行解構
    let (lang, year, is_awesome) = data;
    println!("Language: {}", lang);
    println!("Year: {}", year);
    println!("Is awesome: {}", is_awesome);
}
