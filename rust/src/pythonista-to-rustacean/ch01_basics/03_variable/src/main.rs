//! # 變數與可變性
//!
//! ## 📖 概念說明
//!
//! Rust 的變數預設是不可變的（immutable），這是 Rust 能保證記憶體安全的關鍵之一。
//!
//! ```text
//! ┌──────────────────────────────────────────────────────────────────┐
//! │  變數可變性三层概念                                              │
//! ├──────────────────────────────────────────────────────────────────┤
//! │                                                                  │
//! │  1. 不可變 (let x = 5)                                          │
//! │     ┌─────────────┐                                              │
//! │     │     x = 5    │ ─→ 無法再賦值                               │
//! │     └─────────────┘                                              │
//! │                                                                  │
//! │  2. 可變 (let mut x = 5)                                        │
//! │     ┌─────────────┐                                              │
//! │     │     x = 5    │ ─→ 可以賦值 x = 10                          │
//! │     └─────────────┘                                              │
//! │                                                                  │
//! │  3. 遮蔽 (let x = 5; let x = x + 1)                            │
//! │     ┌─────────────┐     ┌─────────────┐                          │
//! │     │  x = 5 (舊) │ ─→ │  x = 6 (新) │ ← 全新變數，舊的被遮蔽   │
//! │     └─────────────┘     └─────────────┘                          │
//! │                                                                  │
//! └──────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 🔑 與 Python 的對比
//!
//! | Python | Rust | 差異 |
//! |--------|------|------|
//! | `x = 5`（可變） | `let x = 5`（不可變） | Rust 預設不可變 |
//! | `x = 6`（正常） | `let mut x = 5; x = 6`（需加 `mut`） | 需明確宣告可變 |
//! | `x = "hi"; x = 5`（型別可變） | 型別固定 | Rust 為強型別語言 |
//!
//! ## ⚡ Shadowing 與 mut 的差異
//!
//! ```text
//! Shadowing: let x = 5; let x = x + 1;  // 可以改變型別
//! mut:      let mut x = "hello"; x = 5; // ❌ 型別不能改
//! ```
//!
//! ## 📝 執行方式
//!
//! ```bash
//! cd ch01_basics/03_variable
//! cargo run
//! ```

fn main() {
    /// Mutability（可變性）
    let x = 5;
    // x = 6; // 編譯錯誤！

    let mut counter = 0;
    counter += 1; // OK，因為 counter 是可變的

    /// Shadowing（遮蔽）
    let x = 5;

    // 第一次的 x (值為 5) 被「遮蔽」，現在 x 是一個全新的變數
    let x = x + 1;
    {
        // 在這個內部區塊中，x (值為 6) 又被遮蔽了
        let x = x * 2;
        println!("x in inner scope: {}", x); // 印出 12
    }

    // 內部區塊結束，x = 12 的遮蔽失效
    println!("x outside: {}", x); // 印出 6

    let spaces = " "; // spaces 是一個字串
    let spaces = spaces.len(); // spaces 被遮蔽，現在是一個數字

    let mut buffer = String::new();
    buffer.push_str("hello");
    // ... 完成了對 buffer 的修改 ...

    let buffer = buffer;
    // buffer.push_str("world"); // 編譯錯誤！ buffer 現在不可變了

    /// Constant（常數）
    const MAX_USERS: u32 = 1000;
}
