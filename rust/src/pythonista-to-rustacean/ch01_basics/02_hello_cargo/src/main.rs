//! # Cargo 專案範本
//!
//! ## 📖 概念說明
//!
//! 使用 `cargo new` 命令建立新專案時，Cargo 會自動生成標準備案結構。
//!
//! ```text
//! ┌──────────────────────────────────────────────────────────────┐
//! │  cargo new 產生的檔案結構                                    │
//! ├──────────────────────────────────────────────────────────────┤
//! │                                                              │
//! │  project_name/                                              │
//! │  ├── Cargo.toml          ← 專案設定（專案名稱、版本、相依套件）│
//! │  │                                                              │
//! │  └── src/                                                    │
//! │      └── main.rs          ← 進入點                            │
//! │                                                              │
//! │  💡 `cargo build` 會在專案根目錄產生 `Cargo.lock`           │
//! │                                                              │
//! └──────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 🔑 Cargo 常用命令
//!
//! | 命令 | 說明 |
//! |------|------|
//! | `cargo new name` | 建立新專案 |
//! | `cargo build` | 編譯專案（產生在 `target/debug/`） |
//! | `cargo run` | 編譯並執行 |
//! | `cargo build --release` | 發布編譯（優化，產生在 `target/release/`） |
//!
//! ## 📝 Cargo.toml 範例
//!
//! ```toml
//! [package]
//! name = "my_project"
//! version = "0.1.0"
//! edition = "2021"  # Rust 版本
//!
//! [dependencies]
//! # 在此新增外部套件，例如：serde = "1.0"
//! ```

fn main() {
    println!("Hello, world!");
}
