//! # Deref 與 Box：指標的解參考
//!
//! 本範例展示 Rust 中 `Box`（智能指標）的記憶體模型，
//! 以及 `*`（解參考）與 `&`（取參考）的相互關係。
//!
//! ## Box<T> 的記憶體佈局
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────────┐
//! │              Box<T> 記憶體模型                                    │
//! │                                                                 │
//! │   Stack                          Heap                           │
//! │   ┌─────────────────┐            ┌─────────────────┐          │
//! │   │ sugar_box       │            │                 │          │
//! │   │ (指標，指向 Heap)│ ──────────▶│       20        │          │
//! │   │                 │            │                 │          │
//! │   └─────────────────┘            └─────────────────┘          │
//! │   {:p} = main frame 的位置    {:p} = Heap 的位置             │
//! │                                                                 │
//! │   *sugar_box       → 讀取 Heap 上的值（20）                   │
//! │   &sugar_box      → 指標本身在 Stack 的位址                  │
//! │   &*sugar_box     → Heap 的位址（與 *sugar_box 不同）         │
//! └─────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 指標層次圖
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────────┐
//! │              指標的層次                                        │
//! │                                                                 │
//! │   r1: &Box<u32>          → 指向 Box 本身的參考               │
//! │   **r1                   → 先取 Box，再取 Box 內的值         │
//! │                                                                 │
//! │   r2: &u32               → 直接指向 Heap 資料的參考           │
//! │   *r2                    → 直接讀取 Heap 上的值              │
//! │                                                                 │
//! │   ┌─────────────────────────────────────────────────────────┐ │
//! │   │  r1 ──▶ Box{u32} ──▶ Heap{u32}                       │ │
//! │   │  r2 ──────────────────────▶ Heap{u32}                 │ │
//! │   └─────────────────────────────────────────────────────────┘ │
//! └─────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## Python vs Rust：指標對照
//!
//! | 概念 | Python | Rust |
//! |------|---------|------|
//! | Heap 分配 | `x = [1, 2, 3]`（自動）| `let x = Box::new(20)` |
//! | 解參考 | `x[0]`（自動解包）| `*x`（手動）|
//! | 取參考 | 不需要 | `&x`（明確）|
//! | 多層指標 | `list of lists` | `Box<Box<T>>` |
//!
//! ## 執行方式
//!
//! ```bash
//! cd ch03_ownership/03_deref
//! cargo run
//! ```

fn main() {
    // 1. 初始化
    // 假設糖量是一個需要放在 Heap 上的資料
    let mut sugar_box: Box<u32> = Box::new(20);

    println!("--- 初始狀態 ---");
    println!("sugar_box (Heap 內的值): {}", *sugar_box);
    println!("sugar_box (Stack 本身的位址): {:p}", &sugar_box); // 圖中的 main frame 裡的位置
    println!("sugar_box (指向的 Heap 位址): {:p}", &*sugar_box); // 圖中的 Heap 裡的位置

    // 2. 複製值
    // *sugar_box 嘗試「移動」Heap 上的值。因為 u32 是 Copy 型別，變成了「複製」。
    let a: u32 = *sugar_box;

    println!("\n--- 變數 a (複製) ---");
    println!("a 的值: {}", a);
    println!("a 的位址: {:p}", &a); // 注意：這位址跟上面的 Heap 位址一定不同 (因為在 Stack)

    // 3. 修改 Heap
    // 透過解參考來 "修改" Heap 上的值。
    *sugar_box += 10;

    println!("\n--- 修改 Heap 後 ---");
    println!("sugar_box (新值): {}", *sugar_box);
    println!("a (舊值，應未受影響): {}", a); // 證明 a 是獨立的拷貝

    // 4. 雙重指標 (指向 Stack)
    // r1 是一個指向 "Box 指標本身" (在 Stack 上) 的參考
    let r1: &Box<u32> = &sugar_box;
    let b: u32 = **r1;

    println!("\n--- r1 (指向 Box 本身) ---");
    println!("r1 指向的位址: {:p}", r1); // 這會等於 sugar_box 在 Stack 的位址
    println!("b (透過 **r1 取得): {}", b);

    // 5. 單重指標 (指向 Heap)
    // r2 是一個直接指向 "Heap 數據" (值為 30) 的參考
    let r2: &u32 = &*sugar_box;
    let c: u32 = *r2;

    println!("\n--- r2 (直接指向 Heap 資料) ---");
    println!("r2 指向的位址: {:p}", r2); // 這會等於 sugar_box 指向的 Heap 位址
    println!("c (透過 *r2 取得): {}", c);
}